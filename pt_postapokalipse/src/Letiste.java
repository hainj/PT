
public class Letiste {

	public int X = 0;
	public int Y = 0;
	public int jidlo = 0;
	
	public Letiste(int x, int y) {
		super();
		X = x;
		Y = y;
	}

	public int getX() {
		return X;
	}

	public void setX(int x) {
		X = x;
	}

	public int getY() {
		return Y;
	}

	public void setY(int y) {
		Y = y;
	}

	public int getJidlo() {
		return jidlo;
	}

	public void setJidlo(int jidlo) {
		this.jidlo = jidlo;
	}
	
	
}
